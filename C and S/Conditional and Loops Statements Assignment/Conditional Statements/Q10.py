#Write a program to determine if a given character is a vowel or consonant.
str=str(input("Enter character: "))
if str=='A' or str=='a' or str=='E' or str=='e' or str=='I' or str=='i' or str=='O' or str=='o' or str=='U' or str=='u' :
    print(str, "is vowel.")
else:
    print(str, "is not a vowel.")

